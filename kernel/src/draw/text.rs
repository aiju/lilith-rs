/// Look up Unicode codepoint -> CP437 byte. Returns None if not in CP437.
fn unicode_to_cp437(c: char) -> Option<u8> {
    let cp = c as u32;
    if cp < 0x80 {
        return Some(cp as u8);
    }
    let cp = u16::try_from(cp).ok()?;
    UNICODE_TO_CP437_KEYS
        .binary_search(&cp)
        .ok()
        .map(|i| UNICODE_TO_CP437_VALS[i])
}

static FONT: &'static [u8] = include_bytes!("font.bin");

impl Surface {
    pub fn render_char(&mut self, pos: Point, ch: u8, color: Color) {
        let f = &FONT[14 * ch as usize..14 * (ch as usize + 1)];
        unsafe {
            for j in 0..16 {
                for i in 0..8 {
                    let p = Point {
                        x: pos.x + i,
                        y: pos.y + j,
                    };
                    if self.rect().contains_point(p) {
                        let ptr = self.image_ptr_mut_unsafe(p);
                        let box_drawing = ch >= 0xb0 && ch <= 0xdf;
                        let bit = f[j.min(13) as usize] >> 7 - i & 1 != 0;
                        if (j < 14 || box_drawing) && bit {
                            ptr.write_volatile(color);
                        }
                    }
                }
            }
        }
    }
}

pub struct TextWriter {
    pos: Point,
}

pub static WRITER: IrqLock<TextWriter> = IrqLock::new(TextWriter { pos: Point::ZERO });

impl TextWriter {
    fn write_byte(&mut self, surface: &mut Surface, b: u8) {
        if b == b'\n' {
            self.pos.y += 16;
            self.pos.x = surface.rect().tl.x;
        } else {
            surface.render_char(self.pos, b, Color::WHITE);
            self.pos.x += 8;
            if self.pos.x >= surface.rect().br.x {
                self.pos.x = surface.rect().tl.x;
                self.pos.y += 16;
            }
        }
        if self.pos.y >= surface.rect().br.y {
            surface.self_blit(surface.rect(), Point::new(0, 16));
            self.pos.y -= 16;
        }
    }
    fn write_string(&mut self, surface: &mut Surface, str: &str) {
        for ch in str.chars() {
            self.write_byte(surface, unicode_to_cp437(ch).unwrap_or(0xfe));
        }
    }
}

impl crate::device::Writer for TextWriter {
    fn write(&mut self, data: &[u8]) {
        let mut surface = FRAME_BUFFER.lock();
        for chunk in data.utf8_chunks() {
            self.write_string(&mut surface, chunk.valid());
            if !chunk.invalid().is_empty() {
                self.write_byte(&mut surface, 0xfe);
            }
        }
    }
}
